def test_fit_transform_cities():
    cities = ['Moscow', 'New York', 'Moscow', 'London']
    actual = fit_transform(cities)
    expected = [
        ('Moscow', [0, 0, 1]),
        ('New York', [0, 1, 0]),
        ('Moscow', [0, 0, 1]),
        ('London', [1, 0, 0]),
    ]
    assert actual == expected

def test_len_from_fit_transform():
    alphabet_list = list('abcdefghijklmnopqrstuvwxyz ')
    alphabet_list_encode = fit_transform(alphabet_list)
    assert len(alphabet_list_encode) == len(alphabet_list)

def test_hello_world_in_encode():
    alphabet_list = list('abcdefghijklmnopqrstuvwxyz ')
    h_w_list = list('hello world')
    alphabet_list_encode = fit_transform(alphabet_list)
    h_w_encode_only_codes = list()
    for char in h_w_list:
        i = alphabet_list.index(char)
        h_w_encode_only_codes.append(alphabet_list_encode[i][1])
    assert h_w_encode_only_codes == HELLO_WORLD_ENCODE

